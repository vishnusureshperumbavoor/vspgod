temp={
    "name":"vsp",
    "tech":"public speaker"
}
for x in temp:                  # to loop keys
    print(x)
print()
for x in temp.keys():           # to loop keys
    print(x)        
print()
for x in temp:                  # to loop values
    print(temp[x])  
print()
for x in temp.values():         # to loop values
    print(x)        
print()
for x in temp.items():          # to loop items (keys & values)
    print(x)        